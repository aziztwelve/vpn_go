-- Триал-план теперь выдаёт 2 устройства вместо 1. Причина: у нового юзера
-- почти всегда хотя бы пара устройств (телефон + ноут). Старый лимит в 1
-- создавал трение ещё до первой оплаты. Конверсионный driver переносим
-- на длительность (3 дня) и количество локаций, а не на число устройств.
--
-- На свежих БД 003 уже вставит max_devices=2 (seed обновлён). Эта миграция
-- чинит существующие продакшн-БД, где 003 уже отработал со старым
-- значением 1 — идемпотентный UPDATE.
UPDATE subscription_plans
SET max_devices = 2
WHERE is_trial = true AND max_devices < 2;

-- И подтягиваем уже выданные, но ещё активные триал-подписки —
-- чтобы текущие trial-юзеры тоже получили второй слот без ручного апгрейда.
-- Истёкшие/отменённые не трогаем: это историческая правда.
UPDATE subscriptions
SET max_devices = 2
WHERE status = 'trial'
  AND max_devices < 2
  AND expires_at > NOW();
