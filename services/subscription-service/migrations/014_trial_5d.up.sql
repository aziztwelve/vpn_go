-- ============================================================
-- 014: Триал по умолчанию 5 дней (раньше было 3, см. миграцию 010).
-- ============================================================
-- Кампании с trial_duration_days IS NOT NULL продолжают переопределять
-- длительность через COALESCE в StartTrialTx (task 19). Существующие
-- активные триалы не трогаем — у них expires_at уже зафиксирован,
-- новое значение применится только к будущим StartTrial.
-- ============================================================

UPDATE subscription_plans
SET duration_days = 5
WHERE id = 99 AND is_trial = true;
