-- ============================================================
-- 010: Пересмотр прайса + триал 3 дня по умолчанию.
-- ============================================================
-- Триал откатываем 15 → 3 дней (мигр. 007 → отмена). Существующие активные
-- триалы НЕ трогаем — у subscriptions уже зафиксирован expires_at, новый
-- duration_days применится только к будущим StartTrial.
--
-- Прайс: новые цены для всех тиров (2/3/4/5/10 устр × 1/3/6/12 мес).
-- Множители по длительности от 1м: ×2.5, ×4.5, ×8 — повторяют пропорции
-- предыдущей продакшн-сетки (которая редактировалась поверх БД руками,
-- поэтому в git мы её не видим).
-- Существующие subscriptions не трогаем — у них total_price зафиксирован
-- на момент покупки.
-- ============================================================

-- 1) Триал 3 дня.
UPDATE subscription_plans
SET duration_days = 3
WHERE id = 99 AND is_trial = true;

-- 2) Прайс. UPSERT — обновляет существующие тиры, вставляет 3/4 если их
--    ещё не было.
INSERT INTO device_addon_pricing (plan_id, max_devices, price) VALUES
    -- 1 месяц
    (1, 2,  199.00),
    (1, 3,  220.00),
    (1, 4,  250.00),
    (1, 5,  300.00),
    (1, 10, 700.00),
    -- 3 месяца
    (2, 2,  499.00),
    (2, 3,  549.00),
    (2, 4,  625.00),
    (2, 5,  749.00),
    (2, 10, 1749.00),
    -- 6 месяцев
    (3, 2,  899.00),
    (3, 3,  999.00),
    (3, 4,  1129.00),
    (3, 5,  1349.00),
    (3, 10, 3149.00),
    -- 12 месяцев
    (4, 2,  1599.00),
    (4, 3,  1799.00),
    (4, 4,  1999.00),
    (4, 5,  2399.00),
    (4, 10, 5599.00)
ON CONFLICT (plan_id, max_devices) DO UPDATE SET price = EXCLUDED.price;

-- 3) base_price на subscription_plans = цена за 2 устройства (источник
--    правды — device_addon_pricing, но base_price тоже отдаётся клиентам
--    через ListPlans, так что синхронизируем явно).
UPDATE subscription_plans SET base_price = 199.00  WHERE id = 1;
UPDATE subscription_plans SET base_price = 499.00  WHERE id = 2;
UPDATE subscription_plans SET base_price = 899.00  WHERE id = 3;
UPDATE subscription_plans SET base_price = 1599.00 WHERE id = 4;
